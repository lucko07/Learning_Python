# Dictionary Essentials
"""
Write a program that:
Creates a dictionary describing a car:
"""

car = {
    "brand": "Toyata",
    "model": "Camry",
    "year": 2020
}

# Print the brand
print("Brand:", car["brand"])

# Add a new key "color": "blue"
car["color"] = "blue"

# Update the year to 2023
car["year"] = 2023

# Print the whole dictonary
print("Whole dictionary:", car)
