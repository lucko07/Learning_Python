# COMPARISON OPERATORS
# These operators compare two values and return True or False.
"""
| Operator | Meaning          | Example          |
| -------- | ---------------- | ---------------- |
| `==`     | equal to         | `5 == 5 → True`  |
| `!=`     | not equal        | `5 != 3 → True`  |
| `>`      | greater than     | `10 > 3 → True`  |
| `<`      | less than        | `2 < 5 → True`   |
| `>=`     | greater or equal | `7 >= 7 → True`  |
| `<=`     | less or equal    | `3 <= 10 → True` |
"""

a = 10
b = 5

print("a == b: ", a == b)
print("a != b: ", a != b)
print("a > b: ", a > b)
print("a < b: ", a < b)
print ("a >= b: ", a >= b)
print("a <= b: ", a <= b)
