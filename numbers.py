# integers and floats
a = 10            # int
b = 3.5           # float

# basic math
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)

# floor division (no decimals)
print("Floor division:", a // b)


# exponent (power)
print("Power:", a ** 2)


# modulus (remainder)
print("Remainder:", a % 3)


# type checking
print("Type of a:", type(a))
print("Type of b:", type(b))
